    private void btnDeleteRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteRoomActionPerformed
        // TODO add your handling code here:
        try {
            int row = table.getSelectedRow();
            String Room_ID = table.getModel().getValueAt(row, 0).toString();
            

            int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete " + Room_ID + "?", "Delete", JOptionPane.YES_NO_OPTION);

            if (p == 0) {
                String delete = "DELETE FROM Room WHERE Room_ID = ?";

                try (Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/BHMS", "root", "1234");
                    PreparedStatement statement = connection.prepareStatement(delete)) {

                    statement.setString(1, Room_ID);
                    statement.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Successfully Deleted!");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnDeleteRoomActionPerformed

