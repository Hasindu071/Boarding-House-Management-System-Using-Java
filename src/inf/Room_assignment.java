    private void btnAddroomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddroomActionPerformed
        // TODO add your handling code here:
        
         if(txtRoomID.getText().equals("") || txtDescription.getText().equals("") || txtno_of_beds.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Please enter all data.");
    } else {
        String Room_ID = txtRoom_ID.getText();
        String description = txtroomdescription.getText();
        String noOfBedsText = txtno_of_beds.getText();     

        try {
            int no_of_beds = Integer.parseInt(noOfBedsText);

            Class.forName("com.mysql.cj.jdbc.Driver");
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/BHMS", "root", "1234");

            String sql = "INSERT INTO Room(Room_ID, description, no_of_beds) VALUES (?, ?, ?)";

            try (PreparedStatement Statement = connection.prepareStatement(sql)) {
                Statement.setString(1, Room_ID);
                Statement.setString(2, description);
                Statement.setInt(3, no_of_beds);
                
                int rowsInserted = Statement.executeUpdate();
                
                if(rowsInserted > 0) {
                    JOptionPane.showMessageDialog(this, "Record has been inserted successfully.");
                    DefaultTableModel tblModel = (DefaultTableModel) tblroom.getModel();
                    String data[] = {Room_ID, description, noOfBedsText};
                    tblModel.addRow(data);
                    // Clear text fields after successful insertion
                    txtRoom_ID.setText("");
                    txtroomdescription.setText("");
                    txtno_of_beds.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Record has not been inserted.");
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for the number of beds.");
        } catch (ClassNotFoundException | SQLException e ){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }

    }//GEN-LAST:event_btnAddroomActionPerformed

